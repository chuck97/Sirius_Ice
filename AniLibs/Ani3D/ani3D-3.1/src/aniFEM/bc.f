C ======================================================================
      Subroutine applyDIR(LDA, nRow, A, F, k, bc)
C ======================================================================
      Integer LDA, k
      Real*8  A(LDA, *), F(*), bc
C ======================================================================
      Do i = 1, nRow
         F(i) = F(i) - A(i, k) * bc

         A(i, k) = 0
         A(k, i) = 0
      End do

      F(k) = bc
      A(k, k) = 1

      Return
      End


